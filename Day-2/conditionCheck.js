// IF–ELSE STATEMENT
// If the condition is true, the `if` block will execute.
// Otherwise, the `else` block will execute.


// Example 1: Simple condition check
let num = 20;

if (num === 20) {
  console.log("true");
}


// Example 2: Driving eligibility check
// If age is 18 or more → can drive
// Otherwise → underage

let age = 17; // age is less than 18

if (age >= 18) {
  console.log("You can drive");
} else {
  console.log("You can't drive - You are underage");
}
